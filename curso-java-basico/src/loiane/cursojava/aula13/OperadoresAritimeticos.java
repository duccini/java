package loiane.cursojava.aula13;

public class OperadoresAritimeticos {
    public static void main(String[] args) {
        int resultado = 1+ 2;

        // 3
        System.out.println(resultado);

        // 3, Imprime e depois adiciona 1
        System.out.println(resultado++);

        // 5, Adiciona 1 e depois imprime
        System.out.println(++resultado);
    }

}
